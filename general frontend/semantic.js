// Semantic elements like 
// <article>, <section>, <header>, <nav>, <footer>, <aside>, etc., 
// describe the meaning and purpose of the content they wrap.


// Screen Readers & Assistive Tools:
// Semantic tags give structure — e.g., 
// a screen reader can tell a visually impaired user,
// “You’re in a navigation section” or “This is an article”.

// Keyboard Navigation:
// Users navigating via keyboard (tab, arrow keys) can jump between logical sections 
// (<main>, <nav>, <header>).

// ARIA Landmarks:
// Semantic tags act as implicit landmarks 
// (like role="navigation" or role="main"), 
// reducing the need for extra ARIA roles.

// How They Improve SEO

// Better Content Understanding by Search Engines:
// Search crawlers use semantic tags to interpret what each part of a page means —
// e.g., content inside <article> is treated as a standalone post or blog entry.

// Improved Indexing & Ranking:
// Proper hierarchy using <header>, <section>, <article>, <aside> helps Google structure the page content clearly, improving relevancy signals.

// Rich Snippets and SERP Features:
// Combined with structured data, semantic layout increases the chance of enhanced results (like featured snippets).